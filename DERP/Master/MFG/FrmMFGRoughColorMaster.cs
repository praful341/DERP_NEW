        #region Functions
        private bool SaveDetails()
        {
            bool blnReturn = true;
            MfgRoughColor_MasterProperty MFGRoughColorMasterProperty = new MfgRoughColor_MasterProperty();
            MfgRoughColorMaster objRoughColor = new MfgRoughColorMaster();

            try
            {
                if (!ValidateDetails())
                {
                    blnReturn = false;
                    return blnReturn;
                }

                MFGRoughColorMasterProperty.rough_color_id = Val.ToInt32(lblMode.Tag);
                MFGRoughColorMasterProperty.rough_color_name = txtRoughColorName.Text.ToUpper();
                MFGRoughColorMasterProperty.active = Val.ToBoolean(chkActive.Checked);
                MFGRoughColorMasterProperty.remarks = txtRemark.Text.ToUpper();
                MFGRoughColorMasterProperty.sequence_no = Val.ToInt(txtSequenceNo.Text);

                int IntRes = objRoughColor.Save(MFGRoughColorMasterProperty);
                if (IntRes == -1)
                {
                    Global.Confirm("Error In Save Rough Color Details");
                    txtRoughColorName.Focus();
                }
                else
                {
                    if (Val.ToInt(lblMode.Tag) == 0)
                    {
                        Global.Confirm("Rough Color Details Data Save Successfully");
                    }
                    else
                    {
                        Global.Confirm("Rough Color Details Data Update Successfully");
                    }
                }

            }
            catch (Exception ex)
            {
                General.ShowErrors(ex.ToString());
                blnReturn = false;
            }
            finally
            {
                MFGRoughColorMasterProperty = null;
            }

            return blnReturn;
        }
        private bool ValidateDetails()
        {
            bool blnFocus = false;
            List<ListError> lstError = new List<ListError>();
            try
            {
                if (txtRoughColorName.Text == string.Empty)
                {
                    lstError.Add(new ListError(12, "Rough Color Name"));
                    if (!blnFocus)
                    {
                        blnFocus = true;
                        txtRoughColorName.Focus();
                    }
                }

                if (!objRoughColor.ISExists(txtRoughColorName.Text, Val.ToInt64(lblMode.Tag)).ToString().Trim().Equals(string.Empty))
                {
                    lstError.Add(new ListError(23, "Rough Color Name"));
                    if (!blnFocus)
                    {
                        blnFocus = true;
                        txtRoughColorName.Focus();
                    }
                }

                if (txtSequenceNo.Text == string.Empty || txtSequenceNo.Text == "0")
                {
                    lstError.Add(new ListError(12, "Sequence No"));
                    if (!blnFocus)
                    {
                        blnFocus = true;
                        txtSequenceNo.Focus();
                    }
                }
            }
            catch (Exception ex)
            {
                lstError.Add(new ListError(ex));
            }
            return (!(BLL.General.ShowErrors(lstError)));

        }
