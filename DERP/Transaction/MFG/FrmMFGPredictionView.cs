        public bool show()
        {
            bool blnReturn = true;

            try
            {
                if (!ValidateDetails())
                {
                    blnReturn = false;
                    return blnReturn;
                }
                if (txtLotID.Text != string.Empty || Val.ToInt64(txtLotID.Text) != 0 || Val.ToInt(lueCutNo.EditValue) != 0)
                {
                    GetPrediction(Val.ToInt(lueCutNo.EditValue), Val.ToInt64(txtLotID.Text));
                }
            }
            catch (Exception ex)
            {
                BLL.General.ShowErrors(ex);
                blnReturn = false;
            }
            return blnReturn;
        }
        private bool ValidateDetails()
        {
            bool blnFocus = false;
            List<ListError> lstError = new List<ListError>();

            try
            {

                if (lueCutNo.Text == "" && Val.ToInt64(txtLotID.Text) == 0)
                {
                    lstError.Add(new ListError(13, "Cut No"));
                    lstError.Add(new ListError(12, "Lot No"));

                    if (!blnFocus)
                    {
                        blnFocus = true;
                        lueCutNo.Focus();
                    }
                }

            }

            catch (Exception ex)
            {
                lstError.Add(new ListError(ex));
            }
            return (!(BLL.General.ShowErrors(lstError)));
        }
